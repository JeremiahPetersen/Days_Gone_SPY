const table5Content = document.getElementById("table5-content");
const tableButtons = document.querySelectorAll("button[class=tbl5]");

const createRow = (obj) => {
  const row = document.createElement("tr");
  const objKeys = Object.keys(obj);
  objKeys.map((key) => {
    const cell = document.createElement("td");
    cell.setAttribute("data-attr", key);
    cell.innerHTML = obj[key];
    row.appendChild(cell);
  });
  return row;
};

const getTable5Content = (data) => {
  data.map((obj) => {
    const row = createRow(obj);
    table5Content.appendChild(row);
  });
};

const sortData = (data, param, direction = "asc") => {
  table5Content.innerHTML = "";
  const sortedData =
    direction == "asc"
      ? [...data].sort(function (a, b) {
          if (a[param] < b[param]) {
            return -1;
          }
          if (a[param] > b[param]) {
            return 1;
          }
          return 0;
        })
      : [...data].sort(function (a, b) {
          if (b[param] < a[param]) {
            return -1;
          }
          if (b[param] > a[param]) {
            return 1;
          }
          return 0;
        });
  getTable5Content(sortedData);
};

const resetButtons = (event) => {
  [...tableButtons].map((button) => {
    if (button !== event.target) {
      button.removeAttribute("data-dir");
    }
  });
};

window.addEventListener("load", () => {
  getTable5Content(top100UPMAIN.UPMAIN);

  [...tableButtons].map((button) => {
    button.addEventListener("click", (e) => {
      if (e.target.getAttribute("data-dir") == "desc") {
        sortData(top100UPMAIN.UPMAIN, e.target.id, "desc");
        e.target.setAttribute("data-dir", "asc");
      } else {
        sortData(top100UPMAIN.UPMAIN, e.target.id, "asc");
        e.target.setAttribute("data-dir", "desc");
      }
    });
  });
});

